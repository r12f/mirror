static DWORD s_nWMInfoCount = sizeof(s_vWMInfoList) / sizeof(WMInfo);

CDlgWindowOp::CDlgWindowOp()
    : CLayout(IDD)
    , CUIControl(this)
{
    GetFramework()->RegisterComponent(COMPONENT_UI_DLG_WINDOW_OP, this);

    CString strWMInfo;
    for(DWORD i = 0; i < s_nWMInfoCount; ++i) {
        strWMInfo.Format(_T("%s [0x%x]"), s_vWMInfoList[i].m_pWMInfo, s_vWMInfoList[i].m_nMsg);
        m_vWindowMessages[strWMInfo] = s_vWMInfoList[i].m_nMsg;
    }
}

CDlgWindowOp::~CDlgWindowOp()
{
    GetFramework()->UnregisterComponent(COMPONENT_UI_DLG_WINDOW_OP);
}

LRESULT
CDlgWindowOp::OnInitDialog(HWND hWnd, LPARAM lInitData)
{
    m_oComboMessages.SubclassWindow(GetDlgItem(IDC_COMBO_WI_WINDOW_MESSAGE));
    WMList::iterator oWMIt = m_vWindowMessages.begin();
    while(oWMIt != m_vWindowMessages.end()) {
        m_oComboMessages.AddItem(oWMIt->first, -1, -1, 0, (LPARAM)oWMIt->second);
        ++oWMIt;
    }

    m_oComboMessages.TakeSnapshot();

    m_oEditWParam = GetDlgItem(IDC_EDIT_WI_WPARAM);
    m_oEditWParam.SetWindowText(_T("0"));
    m_oEditLParam = GetDlgItem(IDC_EDIT_WI_LPARAM);
    m_oEditLParam.SetWindowText(_T("0"));

    m_oBtnShowWindow.SubclassWindow(GetDlgItem(IDC_BUTTON_WI_SHOW_WINDOW));
    m_oBtnShowWindow.SetDropDownMenu(IDR_MENU_WI_SHOW_WINDOW);

    m_oBtnEnableWindow.SubclassWindow(GetDlgItem(IDC_BUTTON_WI_ENABLE_WINDOW));
    m_oBtnEnableWindow.SetDropDownMenu(IDR_MENU_WI_ENABLE_WINDOW);

    m_oBtnModifyZorder.SubclassWindow(GetDlgItem(IDC_BUTTON_WI_ZORDER));
    m_oBtnModifyZorder.SetDropDownMenu(IDR_MENU_WI_ZORDER);

    m_oBtnSysCommand.SubclassWindow(GetDlgItem(IDC_BUTTON_WI_SYSCOMMAND));
    m_oBtnSysCommand.SetDropDownMenu(IDR_MENU_WI_SYSCOMMAND);

    m_oBtnSetFocus = GetDlgItem(IDC_BUTTON_WI_SET_FOCUS);
    m_oBtnSetActive = GetDlgItem(IDC_BUTTON_WI_SET_ACTIVE);
    m_oBtnSetForeground = GetDlgItem(IDC_BUTTON_WI_SET_FOREGROUND);

    ProvideHotkey();

    return TRUE;
}

